-- Game config stuff
function love.conf(t)
    
    -- Love2d version
    t.version = "0.9.2"  
    
    -- Title on game window
    t.window.title = "Zombie Game (Pre-Alpha 0.1.2)"

    -- Game icon
    t.window.icon = "images/gameicon.png"

    -- Games window width
    t.window.width = 1280

    -- Games window height
    t.window.height = 720   
end